def matrix_rain(lines=12, speed=0.012):
    chars = "ABCDEFabcdef0123456789!@#$%^&*()_+-=[]{}|;:',.<>?/`~"
    for _ in range(lines):
        color = random.choice([90, 91, 92, 93, 94, 95, 96])
        line = ''.join(random.choice(chars) for _ in range(55))
        print(f"\033[38;5;{color}m{line}{X}")
        time.sleep(speed)

def progress_bar(task="VERIFYING", duration=2, steps=30):
    print(f"\n{Y}[*] {task}...{X}")
    for i in range(steps + 1):
        bar = '█' * i + '░' * (steps - i)
        percent = int((i / steps) * 100)
        if percent < 40:
            color = R
        elif percent < 70:
            color = Y
        elif percent < 95:
            color = B
        else:
            color = G
        sys.stdout.write(f"\r{color}[{bar}] {percent}%{X}")
        sys.stdout.flush()
        time.sleep(duration / steps)
    print(f"\n{G}[✓] {task} COMPLETE!{X}\n")

def typewriter(text, color=G, speed=0.025):
    for char in text:
        sys.stdout.write(f"{color}{char}{X}")
        sys.stdout.flush()
        time.sleep(speed)
    print()

def spinner(seconds=1.2, text="Processing"):
    frames = ['◜','◝','◞','◟']
    end = time.time() + seconds
    while time.time() < end:
        for frame in frames:
            sys.stdout.write(f"\r{C}{frame} {text}...{X}")
            sys.stdout.flush()
            time.sleep(0.06)
    sys.stdout.write(f"\r{G}✓ {text} Done!{X}     \n")

